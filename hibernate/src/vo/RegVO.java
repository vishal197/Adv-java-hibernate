package vo;

public class RegVO {

int id;
String fn;
String ln;
String un;
String pw;

public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
public String getFn() {
	return fn;
}
public void setFn(String fn) {
	this.fn = fn;
}
public String getLn() {
	return ln;
}
public void setLn(String ln) {
	this.ln = ln;
}
public String getUn() {
	return un;
}
public void setUn(String un) {
	this.un = un;
}
public String getPw() {
	return pw;
}
public void setPw(String pw) {
	this.pw = pw;
}
}
